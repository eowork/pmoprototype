// Helper function to generate radar chart data
export function generateRadarChartData(data: StudentParityData[], type: 'admission' | 'graduation'): RadarChartData[] {
  return data
    .filter(item => item.status === 'approved') // Only approved data
    .map(item => ({
      program: item.program,
      malePercentage: type === 'admission' ? item.admissionMale : item.graduationMale,
      femalePercentage: type === 'admission' ? item.admissionFemale : item.graduationFemale
    }));
}
